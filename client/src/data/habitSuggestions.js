export function getHabitSuggestionsForValue(value) {
  // Exact match
  if (habitSuggestions[value]) return habitSuggestions[value]

  // Partial match (case insensitive)
  const lowerValue = value.toLowerCase()
  for (const [key, suggestions] of Object.entries(habitSuggestions)) {
    if (lowerValue.includes(key.toLowerCase()) || key.toLowerCase().includes(lowerValue)) {
      return suggestions
    }
  }

  // Default suggestions
  return habitSuggestions.default
}
